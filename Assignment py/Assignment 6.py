students = ("John", "Emily", "Alice", "Bob", "Charlie")

# Print second student
print("Second student:", students[1])

# Check if Alice is in the tuple
print("Is Alice in the tuple?", "Alice" in students)

# Concatenate with new students
new_students = ("David", "Eve", "Frank")
all_students = students + new_students

# Print final tuple and length
print("All students:", all_students)
print("Total number of students:", len(all_students))
