var name;                   /* Declare a Variable */
name= "Aditi Mahesh Rajguru";      /* Initializing the value */
// name= "Kavita Mahesh Rajguru";
console.log(name);
/* In var, the value can be over-write. That means it can change the existing value of the variable. It can be re-declare. */

let name1= "Shriya";
// name1= "Aarya";
console.log(name1);
/* In let, the value of the variable can be change. But cannot re-declare the same variable. */

const pi= 3.14;
console.log(pi);
/* In const, the value cannot be re-declare or over-write. */